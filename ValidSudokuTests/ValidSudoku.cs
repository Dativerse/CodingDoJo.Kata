    private bool IsValidSudoku(char[][] board)
    {
        var cacheStorage = new Dictionary<string, char[]>();
        for (var i = 0; i < board.Length; i++)
        {
            cacheStorage.Add($"row:{i}", new char[9]);
            cacheStorage.Add($"col:{i}", new char[9]);
            cacheStorage.Add($"box:{i}", new char[9]);
        }

        for (var row = 0; row < 9; row++)
        {
            for (var col = 0; col < 9; col++)
            {
                if (board[row][col] == '.')
                {
                    continue;
                }
                
                if (cacheStorage.TryGetValue($"row:{row}", out var rowValue))
                {
                    if (rowValue.Contains(board[row][col]))
                    {
                        return false;
                    }
                    
                    rowValue[col] = board[row][col];
                }

                if (cacheStorage.TryGetValue($"col:{col}", out var colValue))
                {
                    if (colValue.Contains(board[row][col]))
                    {
                        return false;
                    }
                    
                    colValue[row] = board[row][col];
                }

                var boxKey = (row / 3) * 3 + col / 3;
                if (cacheStorage.TryGetValue($"box:{boxKey}", out var boxValue))
                {
                    if (boxValue.Contains(board[row][col]))
                    {
                        return false;
                    }
                    
                    var boxIndex = (row % 3) + (col % 3);
                    boxValue[boxIndex] = board[row][col];
                }
            }
        }
        
        return true;
    }
